    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean encryption = false;
        if(jRadioButton1.isSelected()) encryption=true;
        else if(jRadioButton2.isSelected()){}
        else{jTextArea1.setText("Please provide valid encryption / decryption choice"); return;}
        if(jLabel9.getText().equals("0")||jLabel10.getText().equals("0")||jLabel11.getText().equals("0")){
             jTextArea1.setText("Please generate keys first");
             return;
        }
        String msg = jTextArea1.getText();
        if(msg.equals("")&&encryption){
            jTextArea1.setText("Please enter text to proceed");
            return;
        }
        String afterMsg;
        if(encryption){
            afterMsg=RSA.encrypt(msg,Long.parseLong(jLabel10.getText()) , Long.parseLong(jLabel11.getText()));
        }
        else{
            afterMsg=RSA.decrypt(msg,Long.parseLong(jLabel9.getText()) , Long.parseLong(jLabel11.getText()));
        }
        jTextArea2.setText(afterMsg);
    }//GEN-LAST:event_jButton1ActionPerformed

